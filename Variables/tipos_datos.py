#Ejemplo de tipos de datos en python

#Entero
edad = 25
print('edad:', edad) #25

#Numeri con punto flotante
altura = 1.72
print('altura:', altura) #1.72

#cadena de texto
nombre = "Ana"
print('nombre:', nombre) #Ana

#Tipo booleano
es_casado = True
print('es casado:?', es_casado) #True

#None  
direccion = None
print('direccion:', direccion) #None